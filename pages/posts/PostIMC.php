      <h2>🧠 O que é IMC?</h2>
      <p>O IMC é uma fórmula matemática que relaciona seu peso com sua altura. O valor obtido é comparado a uma tabela padronizada pela Organização Mundial da Saúde (OMS) para identificar a faixa de peso ideal.</p>
  
      <p><strong>Fórmula:</strong> IMC = Peso (kg) / Altura² (m)</p>
  
      <h2>📊 Classificação do IMC</h2>
      <ul>
        <li>🔵 Menor que 18,5 — Abaixo do peso</li>
        <li>🟢 Entre 18,5 e 24,9 — Peso normal</li>
        <li>🟡 Entre 25 e 29,9 — Sobrepeso</li>
        <li>🟠 Entre 30 e 34,9 — Obesidade grau I</li>
        <li>🔴 Entre 35 e 39,9 — Obesidade grau II</li>
        <li>⚫ 40 ou mais — Obesidade grau III (mórbida)</li>
      </ul>
  
