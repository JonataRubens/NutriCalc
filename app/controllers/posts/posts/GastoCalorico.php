  <h2>🔥 O que é gasto calórico?</h2>
  <p>Gasto calórico é a quantidade de energia (calorias) que seu corpo utiliza para funcionar. Isso inclui funções básicas como respiração, digestão e circulação, além das atividades físicas diárias.</p>

  <h2>🧮 Como funciona nossa calculadora?</h2>
  <ul>
    <li>Você informa seu sexo, idade, peso, altura e nível de atividade física.</li>
    <li>A calculadora utiliza fórmulas como a <strong>Mifflin-St Jeor</strong> para estimar sua Taxa Metabólica Basal (TMB).</li>
    <li>Multiplicamos a TMB pelo fator de atividade para obter o gasto calórico diário estimado.</li>
  </ul>

  <h2>📚 Base científica e referências</h2>
  <ol>
    <li>Equação de Mifflin-St Jeor — uma das mais confiáveis para estimar o metabolismo basal.</li>
    <li>Guia da Organização Mundial da Saúde sobre níveis de atividade física.</li>
    <li>Diretrizes nutricionais do Ministério da Saúde.</li>
  </ol>

